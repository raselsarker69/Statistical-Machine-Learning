# Linear Space Array
import numpy as np

# Generating a linear space array with evenly spaced values
linear_space = np.linspace(0, 10, 5)

print('Linear Space Array:', linear_space)


# 0 is the starting value of the range.
# 10 is the ending value of the range.
# 5 specifies the number of evenly spaced values to generate within the specified range 0-> 2.5--> 5.--> 7.2